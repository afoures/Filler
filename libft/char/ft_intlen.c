#include "libft.h"

size_t		ft_intlen(int n)
{
	size_t	len;
	size_t	nb;

	len = 1;
	nb = (n < 0) ? -n : n;
	if (n < 0)
		len++;
	while ((nb /= 10) > 0)
		len++;
	return (len);
}
