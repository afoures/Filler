#include "libft.h"

int		ft_atoi(const char *str)
{
	int	neg;
	int nb;
	int i;

	i = 0;
	nb = 0;
	neg = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if ((str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		nb *= 10;
		nb += ((int)str[i] - '0');
		i++;
	}
	return (neg * nb);
}
