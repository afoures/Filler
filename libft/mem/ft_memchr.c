#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;
	int				i;

	str = (unsigned char *)s;
	i = 0;
	while (n-- > 0)
	{
		if (*str == (unsigned char)c)
			return ((void *)(s + i));
		str++;
		i++;
	}
	return (NULL);
}
